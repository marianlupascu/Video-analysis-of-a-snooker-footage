def process_colors(colors):
    result = {
        "white" : 0,
        "red" : 0,
        "blue" : 0,
        "pink" : 0,
        "dark_green" : 0,
        "brown" : 0,
        "yellow" : 0,
        "black" : 0
    }

    for color in colors:
        if color[0] != "green":
            result[color[0]] += 1
        else:
            if color[1] != "pink" and color[2] == "pink":
                result["pink"] += 1
            else:
                if color[1] == "white" and color[2] == "dark_green":
                    result["dark_green"] += 1
                else:
                    result[color[1]] += 1
    
    if result["white"] > 1:
        result["white"] = 1
    if result["black"] > 1:
        result["black"] = 1
    if result["yellow"] > 1:
        result["red"] = result["red"] + result["yellow"] - 1
        result["yellow"] = 1
    if result["brown"] > 1:
        result["red"] = result["red"] + result["brown"] - 1
        result["brown"] = 1
    if result["pink"] > 1:
        result["red"] = result["red"] + result["pink"] - 1
        result["pink"] = 1

    if result["dark_green"] == 2 and result["blue"] == 0:
        result["dark_green"] = 1
        result["blue"] = 1
    if result["dark_green"] == 0 and result["blue"] == 2:
        result["dark_green"] = 1
        result["blue"] = 1

    if result["dark_green"] > 1:
        result["dark_green"] = 1

    if result["blue"] > 1:
        result["blue"] = 1

    num_balls = 0
    for _, number in result.items(): 
        num_balls += number

    return result, num_balls

